    def _calc_drag_coefficient(self, params: Dict, manifest: ResultManifest) -> Dict:
        """Calculate drag coefficient"""
        if self.is_mock:
            return {"value": 0.0, "unit": "-", "coefficient_type": "drag"}

        # Real calculation: try to read from OpenFOAM forceCoeffs
        result_root = manifest.result_root if manifest else None
        if result_root:
            coeffs_file = Path(result_root) / "postProcessing" / "forceCoeffs" / "0" / "coefficient.dat"
            if coeffs_file.exists():
                try:
                    import numpy as np
                    # Read OpenFOAM forceCoeffs output
                    data = np.loadtxt(coeffs_file, skiprows=1)  # Skip header
                    if len(data) > 0:
                        # Cd is typically column 1 (Time, Cd, Cl, Cm)
                        cd = data[-1, 1] if data.shape[1] > 1 else 0.0
                        return {
                            "value": float(cd),
                            "unit": "-",
                            "coefficient_type": "drag",
                            "source": "forceCoeffs",
                        }
                except Exception:
                    pass

        # Fallback: synthetic value
        return {"value": 0.025, "unit": "-", "coefficient_type": "drag", "source": "synthetic"}

    def _calc_lift_coefficient(self, params: Dict, manifest: ResultManifest) -> Dict:
        """Calculate lift coefficient"""
        if self.is_mock:
            return {"value": 0.0, "unit": "-", "coefficient_type": "lift"}

        # Real calculation: try to read from OpenFOAM forceCoeffs
        result_root = manifest.result_root if manifest else None
        if result_root:
            coeffs_file = Path(result_root) / "postProcessing" / "forceCoeffs" / "0" / "coefficient.dat"
            if coeffs_file.exists():
                try:
                    import numpy as np
                    data = np.loadtxt(coeffs_file, skiprows=1)
                    if len(data) > 0 and data.shape[1] > 2:
                        cl = data[-1, 2]  # Cl is column 2
                        return {
                            "value": float(cl),
                            "unit": "-",
                            "coefficient_type": "lift",
                            "source": "forceCoeffs",
                        }
                except Exception:
                    pass

        return {"value": 0.15, "unit": "-", "coefficient_type": "lift", "source": "synthetic"}

