def execute_visualization(
    action_plan: ActionPlan,
    manifest: ResultManifest,
    output_root: Optional[str] = None,
    case_name: Optional[str] = None,
    is_mock: bool = True,
) -> ActionLog:
    """
    Convenience function to execute visualization

    Args:
        action_plan: ActionPlan to execute
        manifest: ResultManifest with available resources
        output_root: Optional output root directory
        case_name: Optional case name
        is_mock: If True, use placeholder mode (default for tests).
                If False, use real matplotlib backend.

    Returns:
        ActionLog with execution results
    """
